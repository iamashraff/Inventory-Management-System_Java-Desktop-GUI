    //METHOD TO EXPORT JTABLE VALUE TO TEXT FILE
    private void exporttoFile(){
            //USING TRY CATCH
           try{
                String strPath = "C:/Export.txt"; //EXPORT TO THIS PATH
                //FILE PATH
               File file = new File(strPath);
               
                //CREATE FILE IF NOT EXISTS
               if(!file.exists()){
                   file.createNewFile();
               }//END IF
               
               //CREATE OBJECT FILEWRITER AND BUFFEREDWRITER
               FileWriter fw = new FileWriter(file.getAbsoluteFile());
               BufferedWriter bw = new BufferedWriter(fw);
               
               //loop for jtable rows
               for(int i = 0; i < jTable1.getRowCount(); i++){
                   //loop for jtable column
                   for(int j = 0; j < jTable1.getColumnCount(); j++){
                       bw.write(jTable1.getModel().getValueAt(i, j)+" ");
                   }//END INNER LOOP
               bw.write("\n");
               }//END OUTER LOOP
               bw.write("_____________________________________________\n"
                       + "Generated by Inventory Management System");
               //close BufferedWriter
               bw.close();
               //close FileWriter 
               fw.close();
               playExclamation();
               new msgBox("Table has been exported to " + strPath,"Data Exported").showinfoMsg();
               
               }catch(Exception ex){
                   ex.printStackTrace();
               }//END TRY CATCH
           } //END METHOD
    

