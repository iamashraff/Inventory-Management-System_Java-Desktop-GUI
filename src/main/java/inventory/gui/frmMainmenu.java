    //METHOD TO LOAD DATA OF PRODUCTS FROM TEXT FILE TO JTABLE
    public void loadData(){
        String filePath = "C:/Products.txt";
        File file = new File(filePath);
        //USING TRY CATCH
        try{
             BufferedReader br = new BufferedReader(new FileReader(file));
            // get the first line
            // get the columns name from the first line
            // set columns name to the jtable model
            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();

            // get lines from txt file
            Object[] tableLines = br.lines().toArray();
            
            // extratct data from lines
            // set data to jtable model
            for(int i = 0; i < tableLines.length; i++)
            {
                String line = tableLines[i].toString().trim();
                String[] dataRow = line.split(",");
                model.addRow(dataRow);
            }
            
            //TO REMOVE NULL PRODUCTS FROM JTABLE
            int i = 0;
            while(i < model.getRowCount()) {
                if (((String)model.getValueAt(i, 1)).equals("null")) {
                model.removeRow(i);
                }else {
                ++i;
                }//END IF
            }//END DO
            
            //SHOW TOTAL PRODUCTS
            lblitemNo.setText(String.valueOf(model.getRowCount())); 
            
            //SHOW TOTAL QUANTITY
             int totalQty = 0;
                for(int j = 0; j < jTable1.getRowCount(); j++){
                    totalQty = totalQty + Integer.parseInt(jTable1.getValueAt(j, 3).toString());
                }//END DO
            lbltotQty.setText(Integer.toString(totalQty));
            
            //SHOW INVENTORY VALUE
            double totalInv = 0;
            for(int k = 0; k < jTable1.getRowCount(); k++){
                    int prodQty = Integer.parseInt(jTable1.getValueAt(k, 3).toString());
                    double prodPrice = Double.parseDouble(jTable1.getValueAt(k, 4).toString());
                    double totalPrice = prodQty * prodPrice;
                    totalInv = totalInv + totalPrice;
                }//END DO
            lblinventoryVal.setText(String.format("  RM %,.2f  ", totalInv));
        
        }catch (FileNotFoundException e) {
            new msgBox("Unable to load data to table. File "+ filePath + " not found !" ,"ERROR").showerrorMsg();
        }catch(Exception e){
            new msgBox("Unknown error occured !","ERROR").showerrorMsg();

       }//END TRY CATCH
        
    }//END METHOD
   
