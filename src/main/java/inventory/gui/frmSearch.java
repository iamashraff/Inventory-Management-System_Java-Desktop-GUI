    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed

        //USING TRY CATCH
        //TO GET SELECTED ROW NO OF JTABLE FOR FETCHING PRODUCT ID
        try {
            
            //GET SELECTED VALUE OF ROW, COLUMN AND PRODUCT ID
            int selectedRowIndex = jTable1.getSelectedRow();
            int getselectedId = Integer.parseInt(jTable1.getValueAt(selectedRowIndex, 0).toString()); //TO GET ROW NUMBER

            //CREATE NEW FRAME EDIT
            frmEdit prodEdit = new frmEdit();

            //INVOKE METHOD LOAD DATA WITH PARAMETER OF SELECTED PRODUCT ID
            prodEdit.loadData(getselectedId);

            //SHOW EDIT FRAME
            prodEdit.setVisible(true);
            dispose();
        }
        //CATCH ARRAY INDEX OUT OF BOUND IF NONE OF THE JTABLE ROW IS SELECTED
        catch (ArrayIndexOutOfBoundsException e) {
            //CREATE OBJECT AND INVOKE METHOD TO SHOW WARNING MESSAGE BOX
            playExclamation();
            new msgBox("No Product Selected.\nSelect at least one product to continue.","WARNING").showwarningMsg();
        }//END TRY CATCH
    }//GEN-LAST:event_btnEditActionPerformed

