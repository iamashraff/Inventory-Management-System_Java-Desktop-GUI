    //METHOD TO LOAD DATA INTO JTABLE
    public void loadData(){
        String filePath = "C:/Products.txt";
        File file = new File(filePath);
        //USING TRY CATCH
        try{
            model = (DefaultTableModel)jTable1.getModel();
             BufferedReader br = new BufferedReader(new FileReader(file));
            // get the first line
            // get the columns name from the first line
            // set columns name to the jtable model

            
            // get lines from txt file
            Object[] tableLines = br.lines().toArray();
            
            // extratct data from lines
            // set data to jtable model
            for(int i = 0; i < tableLines.length; i++)
            {
                String line = tableLines[i].toString().trim();
                String[] dataRow = line.split(",");
                model.addRow(dataRow);
            }
            
            //TO REMOVE NULL PRODUCTS FROM JTABLE
            int i = 0;
            while(i < model.getRowCount()) {
                if (((String)model.getValueAt(i, 1)).equals("null")) {
                model.removeRow(i);
                }else {
                ++i;
                }//END IF
            }//END DO
            
        }catch(Exception ex){
            new msgBox("Unable to load data !","ERROR").showerrorMsg();
       }//END TRY CATCH
    }//END METHOD
    
    //METHOD TO FILTER JTABLE FOR SPECIFIC VALUE OF SEARCH STRING
    public void searchTable(String searchStr){
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(model);
        jTable1.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter("(?i)" + searchStr));
        
        lblsearchFor.setText("Search for : '" + searchStr + "'" );
        
         //SHOW ROWS FOUND
             int noRows = 0;
             noRows = jTable1.getRowCount();
             
             //USING IF ELSE STRUCTURE
             if (noRows<1){
                 lblrowsFound.setText("No result found !");
                 lblrowsFound.setForeground(Color.red);
             }else
             if (noRows==1){
                 lblrowsFound.setText(Integer.toString(noRows) + " result found !");
             }else{
                lblrowsFound.setText(Integer.toString(noRows) + " results found !");
             }//END IF
        
    }//END METHOD

